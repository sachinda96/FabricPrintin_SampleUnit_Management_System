package lk.ijse.fps.dto;

/**
 *
 * @author ranjith-suranga
 */
public class CustomerDTO extends SuperDTO{
    
    private int customerID;
    private String Firstname;
    private String Lastname;
    private String Company;
    private String telNumber;
    private String Email;

    public CustomerDTO() {
    }

    public CustomerDTO(int customerID, String Firstname, String Lastname, String Company, String telNumber, String Email) {
        this.customerID = customerID;
        this.Firstname = Firstname;
        this.Lastname = Lastname;
        this.Company = Company;
        this.telNumber = telNumber;
        this.Email = Email;
    }

    /**
     * @return the customerID
     */
    public int getCustomerID() {
        return customerID;
    }

    /**
     * @param customerID the customerID to set
     */
    public void setCustomerID(int customerID) {
        this.customerID = customerID;
    }

    /**
     * @return the Firstname
     */
    public String getFirstname() {
        return Firstname;
    }

    /**
     * @param Firstname the Firstname to set
     */
    public void setFirstname(String Firstname) {
        this.Firstname = Firstname;
    }

    /**
     * @return the Lastname
     */
    public String getLastname() {
        return Lastname;
    }

    /**
     * @param Lastname the Lastname to set
     */
    public void setLastname(String Lastname) {
        this.Lastname = Lastname;
    }

    /**
     * @return the Company
     */
    public String getCompany() {
        return Company;
    }

    /**
     * @param Company the Company to set
     */
    public void setCompany(String Company) {
        this.Company = Company;
    }

    /**
     * @return the telNumber
     */
    public String getTelNumber() {
        return telNumber;
    }

    /**
     * @param telNumber the telNumber to set
     */
    public void setTelNumber(String telNumber) {
        this.telNumber = telNumber;
    }

    /**
     * @return the Email
     */
    public String getEmail() {
        return Email;
    }

    /**
     * @param Email the Email to set
     */
    public void setEmail(String Email) {
        this.Email = Email;
    }

    
}
